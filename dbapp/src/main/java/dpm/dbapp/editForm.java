  public boolean validates()
  {
      if(txtempid.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter employee id");
          txtempid.grabFocus();
          return false;
      }
      if(txtempname.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter employee Name");
          txtempname.grabFocus();
          return false;
      }
      if(txtleavetype.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter Leave Type");
          txtleavetype.grabFocus();
          return false;
      }
      if(txtleavedate.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter Leave Date");
          txtleavedate.grabFocus();
          return false;
      }
      if(txtcharge.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter Employee Name");
          txtcharge.grabFocus();
          return false;
      }
      if(txtindate.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(editForm.this, "Enter In Date");
          txtindate.grabFocus();
          return false;
      }
      return true;
  }
