    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(validates())
        {try {
              String sqlstr = "UPDATE empleave SET empid = ?, empname = ?, leavetype = ?, leavedate = ?, charge = ?, indate = ? WHERE empid = ?";
              //System.out.println(sqlstr);
              st = (PreparedStatement) conn.prepareStatement(sqlstr);
              
              st.setString(1, txtempid.getText());
              st.setString(2, txtempname.getText());
              st.setString(3, txtleavetype.getText());
              st.setString(4, txtleavedate.getText());
              st.setString(5, txtcharge.getText());
              st.setString(6, txtindate.getText());
              st.setString(7, cmbempid.getSelectedItem().toString());

            int rowsUpdated = st.executeUpdate();
              
            
            if(rowsUpdated > 0)
            {
                JOptionPane.showMessageDialog(editForm.this, "Data inserted Successfully!!");
                this.setVisible(false);
                new MainUI().setVisible(true);
                conn.close();
            }
            else
            {
                JOptionPane.showMessageDialog(editForm.this, "Enter correct Data");
                
            }
         
        }
        catch (SQLException sqlException) {
                    sqlException.printStackTrace();
                }
        
        }
    }//GEN-LAST:event_btnEditActionPerformed

